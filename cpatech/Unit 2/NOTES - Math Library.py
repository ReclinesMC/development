import math

print("The .sqrt() function")
print(math.sqrt(25))
print(math.sqrt(100))

print("\nA mathematical constant")
print(math.pi)

print("\nThe .ceil( function")
print(math.ceil(math.pi))
print(math.ceil(5.9))
print(math.ceil(-3.256))

print("\nThe .floor() function")
print(math.floor(math.pi))
print(math.floor(5.9))
print(math.floor(-3.256))
